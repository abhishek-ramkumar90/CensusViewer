package com.mars.distribution.ServicePojo;
import java.util.Collection;

import javax.xml.bind.annotation.XmlRootElement;
@XmlRootElement(name="regionshift")
public class RegionShift {
	
private String zoneId;
private String stateId;
private String districtId;
private Collection<RegionShift> RegionCollection;

public Collection<RegionShift> getRegionCollection() {
	return RegionCollection;
}
public void setRegionCollection(Collection<RegionShift> regionCollection) {
	RegionCollection = regionCollection;
}
public String getZoneId() {
	return zoneId;
}
public void setZoneId(String zoneId) {
	this.zoneId = zoneId;
}
public String getStateId() {
	return stateId;
}
public void setStateId(String stateId) {
	this.stateId = stateId;
}
public String getDistrictId() {
	return districtId;
}
public void setDistrictId(String districtId) {
	this.districtId = districtId;
}
	
}
